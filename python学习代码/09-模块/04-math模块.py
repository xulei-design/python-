# 数学计算相关的模块
import math

print(math.factorial(6))  # 求阶乘

print(math.pow(2, 10))  # 1024 幂运算

print(math.floor(12.98))  # 12 向下取整(常用）
print(math.ceil(16.02))  # 17 向上取整（常用）
# round() 内置函数，实现四舍五入到指定位数

# 用 math 模块计算三角函数
print(math.sin(math.pi / 6)) # 弧度 ∏ = 180°
print(math.cos(math.pi / 3))
print(math.tan(math.pi / 2))
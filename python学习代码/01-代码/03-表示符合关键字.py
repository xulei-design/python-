# 标识符：变量,模块名,函数名,类名
# 标识符的命名规则与规范：
# 规则：
# 1. 有字母，数字，下划线组成，不能以数字开头
# 2. 严格区分大小写
# 3. 不能使用关键字

# 规范：建议遵守，遵守会显得专业，并且代码易读
# 1. 顾名思义
# 2. 遵守一定的命名规范
#    1. 小驼峰命名法：第一个单词的首字母小写，以后的每个单词的首字母都大写 userNameAndPassword
#    2. 大驼峰命名法：每个单词的首字母都大写 PersonModel
#    3. 是用下划线连接：user_name_and_password
# 在python里的变量，函数和模块名是用下划线连接；python里的类名使用大驼峰命名法

